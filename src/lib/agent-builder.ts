  /**
   * 获取 Agent 的能力
   */
  getAgentCapabilities(agentId: AgentId): { base: any[]; domain: Record<string, any[]> } {
    const config = this.agentConfigs.get(agentId);
    if (!config) {
      return { base: [], domain: {} };
    }

    // 获取基础能力
    const baseCapabilities: any[] = [];
    for (const capId of config.baseCapabilities) {
      const plugin = this.capabilityRegistry.get(capId);
      if (plugin) {
        baseCapabilities.push({
          id: plugin.skill.id,
          name: plugin.skill.name,
          level: plugin.skill.level,
          description: plugin.skill.description,
          type: plugin.type,
          replicable: true,
        });
      }
    }

    // 获取领域能力
    const domainCapabilities: Record<string, any[]> = {};
    if (config.domainCapabilities) {
      const domain = config.domainCapabilities.domain;
      const caps: any[] = [];
      
      for (const capId of config.domainCapabilities.capabilities) {
        const plugin = this.capabilityRegistry.get(capId);
        if (plugin && plugin.skill.price !== undefined) {
          caps.push({
            id: plugin.skill.id,
            name: plugin.skill.name,
            price: plugin.skill.price,
            description: plugin.skill.description || '',
            domain: domain,
            provider: 'expert',
            version: plugin.version,
          });
        }
      }
      
      if (caps.length > 0) {
        domainCapabilities[domain] = caps;
      }
    }

    // 添加其他领域的能力（从默认模板）
    const domains = ['电商', '金融', '医疗'];
    for (const domain of domains) {
      if (!domainCapabilities[domain]) {
        const template = getDomainCapabilitiesTemplate(agentId, domain);
        const caps: any[] = template.map((skill) => ({
          id: skill.id,
          name: skill.name,
          price: skill.price || 0,
          description: skill.description || '',
          domain: domain,
          provider: 'expert',
          version: '1.0.0',
        }));
        if (caps.length > 0) {
          domainCapabilities[domain] = caps;
        }
      }
    }

    return { base: baseCapabilities, domain: domainCapabilities };
  }

  /**
   * 导出能力配置
   */
  exportCapabilities(agentId: AgentId): any {
    const config = this.agentConfigs.get(agentId);
    if (!config) {
      throw new Error(`Agent 配置不存在: ${agentId}`);
    }

    const basePlugins = config.baseCapabilities
      .map((id) => this.capabilityRegistry.get(id))
      .filter(Boolean);

    const domainPlugins = config.domainCapabilities
      ? config.domainCapabilities.capabilities
          .map((id) => this.capabilityRegistry.get(id))
          .filter(Boolean)
      : [];

    return {
      agentId,
      config,
      baseCapabilities: basePlugins,
      domainCapabilities: domainPlugins,
      exportedAt: new Date().toISOString(),
    };
  }

  /**
   * 导入能力配置
   */
  importCapabilities(config: any): void {
    const { agentId, baseCapabilities, domainCapabilities } = config;

    // 清理旧的能力
    for (const [key, plugin] of this.capabilityRegistry.entries()) {
      if (plugin.agentId === agentId) {
        this.capabilityRegistry.delete(key);
      }
    }

    // 导入新的能力
    for (const plugin of [...baseCapabilities, ...domainCapabilities]) {
      this.registerCapability(plugin);
    }
  }

