  /**
   * 注册能力插件
   */
  registerCapability(plugin: CapabilityPlugin): void {
    this.capabilityRegistry.set(plugin.id, plugin);
  }

  /**
   * 批量注册能力插件（行业能力包）
   */
  registerCapabilityPackage(domain: string, agentId: AgentId): void {
    const skills = getDomainCapabilitiesTemplate(agentId, domain);
    for (const skill of skills) {
      this.registerCapability({
        id: skill.id,
        name: skill.name,
        version: '1.0.0',
        type: 'domain',
        agentId: agentId,
        skill: skill,
        handler: this.createDomainCapabilityHandler(skill.id, domain),
        dependencies: [],
      });
    }
  }

  /**
   * 创建基础能力处理器
   */
  private createBaseCapabilityHandler(capabilityId: string): (context: any) => Promise<any> {
    return async (context: any) => {
      // 基础能力的通用处理逻辑
      switch (capabilityId) {
        case 'task-decomposition':
          return this.handleTaskDecomposition(context);
        case 'coordination':
          return this.handleCoordination(context);
        case 'programming':
          return this.handleProgramming(context);
        case 'data-analysis':
          return this.handleDataAnalysis(context);
        case 'writing':
          return this.handleWriting(context);
        default:
          return { success: true, message: `执行基础能力: ${capabilityId}` };
      }
    };
  }

  /**
   * 创建领域能力处理器
   */
  private createDomainCapabilityHandler(capabilityId: string, domain: string): (context: any) => Promise<any> {
    return async (context: any) => {
      // 领域能力会根据领域调用特定的处理逻辑
      console.log(`[${domain}] 执行领域能力: ${capabilityId}`);
      return {
        success: true,
        message: `执行 ${domain} 领域能力: ${capabilityId}`,
        domain: domain,
      };
    };
  }

  /**
   * 构建 Agent
   */
  buildAgent(agentId: AgentId, domain?: string): Agent {
    const config = this.agentConfigs.get(agentId);
    if (!config) {
      throw new Error(`Agent 配置不存在: ${agentId}`);
    }

    // 如果指定了领域，先注册该领域的领域能力
    if (domain) {
      this.registerCapabilityPackage(domain, agentId);
    }

    // 收集能力
    const capabilities: Skill[] = [];

    // 1. 添加基础能力
    for (const capId of config.baseCapabilities) {
      const plugin = this.capabilityRegistry.get(capId);
      if (plugin) {
        capabilities.push(plugin.skill);
      }
    }

    // 2. 添加领域能力（如果指定了领域）
    if (domain) {
      const domainSkills = getDomainCapabilitiesTemplate(agentId, domain);
      capabilities.push(...domainSkills);
    }

    // 构建 Agent
    const agent: Agent = {
      id: config.id,
      name: config.name,
      role: config.role,
      description: config.description,
      status: AgentStatus.IDLE,
      skills: capabilities,
      maxConcurrentTasks: config.maxConcurrentTasks,
      currentTasks: 0,
      scheduledTasks: [],
      canSendTo: config.canSendTo,
      canReceiveFrom: config.canReceiveFrom,
      createdAt: new Date(),
      lastActiveAt: new Date(),
    };

    return agent;
  }

  /**
   * 快速替换行业能力
   */
  replaceDomainCapabilities(agentId: AgentId, newDomain: string): Agent {
    // 移除旧的领域能力
    for (const [key, plugin] of this.capabilityRegistry.entries()) {
      if (plugin.type === 'domain' && plugin.agentId === agentId) {
        this.capabilityRegistry.delete(key);
      }
    }

    // 注册新的领域能力
    this.registerCapabilityPackage(newDomain, agentId);

    // 更新配置
    const config = this.agentConfigs.get(agentId);
    if (config) {
      config.domainCapabilities = {
        domain: newDomain,
        capabilities: getDomainCapabilitiesTemplate(agentId, newDomain).map((s) => s.id),
      };
    }

    // 重新构建 Agent
    return this.buildAgent(agentId, newDomain);
  }

  /**
   * 获取能力插件
   */
  getCapability(capabilityId: string): CapabilityPlugin | undefined {
    return this.capabilityRegistry.get(capabilityId);
  }

  /**
   * 列出所有能力
   */
  listCapabilities(agentId?: AgentId, type?: 'base' | 'domain'): CapabilityPlugin[] {
    let plugins = Array.from(this.capabilityRegistry.values());

    if (agentId) {
      plugins = plugins.filter((p) => p.agentId === agentId);
    }

    if (type) {
      plugins = plugins.filter((p) => p.type === type);
    }

    return plugins;
  }

