/**
 * 从搜索词扩展出所有同义词
 * @param word 原始搜索词
 * @returns 扩展后的词列表（包含原始词）
 */
export function expandSynonyms(word: string): string[] {
  const expanded = new Set<string>();
  expanded.add(word); // 保留原始词

  for (const [key, synonyms] of Object.entries(SYNONYM_GROUPS)) {
    // 如果搜索词匹配 key 或任何同义词，整组都加入
    if (key === word || synonyms.includes(word)) {
      expanded.add(key);
      for (const s of synonyms) {
        expanded.add(s);
      }
    }
  }

  return Array.from(expanded);
}

/**
 * 批量扩展关键词列表的同义词
 * @param keywords 原始关键词列表
 * @returns 扩展后的关键词列表（去重）
 */
export function expandKeywordsWithSynonyms(keywords: string[]): string[] {
  const expanded = new Set<string>();
  for (const kw of keywords) {
    for (const word of expandSynonyms(kw)) {
      expanded.add(word);
    }
  }
  return Array.from(expanded);
}

/**
 * 从指令文本中提取关键词并扩展同义词
 * 同时返回原始关键词和扩展后的关键词
 */
export function extractAndExpandKeywords(
  text: string, 
  baseKeywords: string[]
): { originalKeywords: string[]; expandedKeywords: string[]; synonymMap: Record<string, string[]> } {
  const synonymMap: Record<string, string[]> = {};
  const expanded = new Set<string>(baseKeywords);

  for (const kw of baseKeywords) {
    const synonyms = expandSynonyms(kw);
    if (synonyms.length > 1) {
      synonymMap[kw] = synonyms.filter(s => s !== kw);
    }
    for (const s of synonyms) {
      expanded.add(s);
    }
  }

  return {
    originalKeywords: baseKeywords,
    expandedKeywords: Array.from(expanded),
    synonymMap,
  };
}
